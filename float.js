// Floats in Javascript 
console.log('--- Floats ---');
// Declarando
console.log('Declarando');
let f = 3.1416;			// DECLARANDO un float
console.log(f);			// -> 3.1416


// Metodos 
console.log('\n- Metodos No destructivos-');

let r = parseFloat(1.1);// Convertir un numero a float
console.log(r);			// RETURN -> 1

r = f.toFixed(3);	// RETURN una aproximacion hacia un numero	
console.log(r);			// -> 3.142



